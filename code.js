function locale() {
  return LOCALE[currentLanguage] || LOCALE.en;
}

function localizedColorBlindTypes() {
  return COLOR_BLIND_TYPE_KEYS.map(function(key) {
    var typeInfo = locale().colorTypes[key];
    return {
      key: key,
      reportTitle: typeInfo.reportTitle,
      reportDescription: typeInfo.reportDescription
    };
  });
}

